#include <iostream>
using namespace std;

int NM[500][500];
int map[19][3][2] = {
	{{0,1}, {0,2}, {0,3}},
	{{1,0}, {2,0}, {3,0}},
	{{1,0}, {1,1}, {1,2}},
	{{0,1}, {1,0}, {2,0}},
	{{0,1}, {0,2}, {1,2}},
	{{1,0}, {2,0}, {2,-1}},
	{{0,1}, {0,2}, {-1,2}},
	{{1,0}, {2,0}, {2,1}},
	{{0,1}, {0,2}, {1,0}},
	{{0,1}, {1,1}, {2,1}},
	{{0,1}, {1,0}, {1,1}},
	{{0,1}, {-1,1}, {-1,2}},
	{{1,0}, {1,1}, {2,1}},
	{{0,1}, {1,1}, {1,2}},
	{{1,0}, {1,-1}, {2,-1}},
	{{0,1}, {0,2}, {-1,1}},
	{{0,1}, {0,2}, {1,1}},
	{{1,0}, {2,0}, {1,1}},
	{{1,0}, {2,0}, {1,-1}}
};


int main()
{
	int a, b, max = 0;
	cin >> a;
	cin >> b;

	for (int i = 0; i < a; i++) {
		for (int j = 0; j < b; j++) {
			cin >> NM[i][j];
		}
	}

	max = 0;
	for (int i = 0; i < a; i++) {
		for (int j = 0; j < b; j++) {
			for (int k = 0; k < 19; k++) {
				bool ok = true;
				int sum = NM[i][j];
				for (int l = 0; l < 3; l++) {
					int x = i + map[k][l][0];
					int y = j + map[k][l][1];
					if (0 <= x && x < a && 0 <= y && y < b)
					{
						sum += NM[x][y];
					}
					else
					{
						ok = false;
						break;
					}
				}
				if (ok&&max < sum) {
					max = sum;
				}
			}
		}
	}
	cout << max << '\n';
	return 0;
}